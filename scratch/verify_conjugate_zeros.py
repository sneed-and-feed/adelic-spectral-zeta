# Build character chi
lut_32 = {}
for x1 in [0, 1]:
    for x2 in range(8):
        lut_32[((-1)**x1 * 5**x2) % 32] = (x1, x2)
lut_25 = {}
for x3 in range(20):
    lut_25[(2**x3) % 25] = x3

def chi(n):
    n = int(n)
    if math.gcd(n, 800) > 1: return 0.0 + 0.0j
    x1, _ = lut_32[n % 32]
    x3 = lut_25[n % 25]
    val = (5*x1 + 4*x3) % 10
    return np.exp(2j * np.pi * val / 10)

# Galois automorphisms of Q(zeta_20)
# Gal(Q(zeta_20)/Q) = (Z/20Z)* = {1, 3, 9, 7, 11, 13, 19, 17}
# The automorphisms that stabilize the coefficient field K = Q(zeta_12) (which is Q(sqrt(-3)))
# are generated by sigma_3: zeta_20 -> zeta_20^3.
# The 4 conjugates are generated by powers of sigma_3: m in {1, 3, 9, 7}.
gal_m = [1, 3, 9, 7]

def get_galois_conjugate_coeffs(j, M=1500):
    """
    Get Dirichlet coefficients a_n for the j-th Galois conjugate form f^(j).
    j = 0, 1, 2, 3 corresponds to m = gal_m[j] = 1, 3, 9, 7.
    """
    m = gal_m[j]
    
    # Conjugate prime traces
    # The raw Hecke eigenvalue for prime p is a sum of c * zeta_20^k.
    # The Galois action maps this to sum of c * zeta_20^(m*k).
    a_p_conj = {}
    for p in primes_list:
        if p in a_p_complex:
            # Recompute with conjugated zeta
            terms = ap_raw[primes_list.index(p)]
            if not terms:
                a_p_conj[p] = 0.0 + 0.0j
            else:
                a_p_conj[p] = sum(c * (zeta20 ** ((m * k) % 20)) for c, k in terms)
        else:
            a_p_conj[p] = 0.0 + 0.0j

    # Conjugate character
    # The character chi is a homomorphism to roots of unity.
    # The Galois action m acts on chi(p).
    # Since chi(p) has order 10, it is in Q(zeta_20), so chi^(j)(p) = chi(p)^m.
    def chi_conj(n):
        c_val = chi(n)
        if abs(c_val) < 1e-10:
            return 0.0 + 0.0j
        # Find power of zeta_10
        angle = np.angle(c_val) / (2.0 * np.pi)
        k = int(round(angle * 10)) % 10
        return np.exp(2j * np.pi * ((k * m) % 10) / 10)

    # Build a_n up to M
    a = np.zeros(M+1, dtype=complex)
    a[1] = 1.0
    primes_subset = [p for p in primes_list if p <= M]
    
    for p in primes_subset:
        ap = a_p_conj.get(p, 0.0+0j)
        chip = chi_conj(p)
        pk_coeffs = [1.0+0j, ap]
        k = 2
        while p**k <= M:
            pk_coeffs.append(ap * pk_coeffs[k-1] - chip * pk_coeffs[k-2])
            k += 1
        for k in range(1, len(pk_coeffs)):
            if p**k <= M:
                a[p**k] = pk_coeffs[k]

    for i in range(2, M+1):
        if abs(a[i]) < 1e-14:
            continue
        for p in primes_subset:
            if i * p > M:
                break
            if math.gcd(i, p) > 1:
                continue
            ap = a_p_conj.get(p, 0.0+0j)
            chip = chi_conj(p)
            pk_coeffs = [1.0+0j, ap]
            kk = 2
            while i * p**kk <= M:
                pk_coeffs.append(ap * pk_coeffs[kk-1] - chip * pk_coeffs[kk-2])
                kk += 1
            for k in range(1, len(pk_coeffs)):
                pk = p**k
                if i * pk > M:
                    break
                a[i*pk] = a[i] * pk_coeffs[k]
                
    return a

