# Fast AFE evaluator
M_sum = 1200
log_n = np.log(np.arange(1, M_sum+1))

def compute_S1_S2_P(t, j):
    """Compute S1, S2, and P for conjugate form j at t."""
    a_arr = a_conjs[j][1:] # 1..M
    s = 0.5 + 1j*t
    
    # S1
    powers = np.exp(-s * log_n[:M_sum])
    S1 = np.dot(a_arr[:M_sum], powers)
    
    # S2
    s2 = 0.5 - 1j*t
    powers2 = np.exp(-s2 * log_n[:M_sum])
    S2 = np.dot(np.conj(a_arr[:M_sum]), powers2)
    
    # Phase P
    gamma_s = mpmath.gamma(mpmath.mpc(0.5, t))
    gamma_1ms = mpmath.gamma(mpmath.mpc(0.5, -t))
    cond = mpmath.mpf(N) / (4 * mpmath.pi**2)
    cond_factor = mpmath.power(cond, mpmath.mpc(0, t))
    P_mp = gamma_1ms / gamma_s * cond_factor
    P = complex(float(P_mp.real), float(P_mp.imag))
    
    return S1, S2, P

buhler_zeros = [5.1015, 5.5613, 6.0244, 6.4910, 6.9613]

