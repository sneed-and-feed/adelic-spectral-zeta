# Base field generator
zeta20 = np.exp(2j * np.pi / 20.0)

def parse_coeff(terms):
    if not terms: return 0.0 + 0.0j
    return sum(c * (zeta20 ** k) for c, k in terms)

primes_list = list(sp.primerange(2, 2001))
