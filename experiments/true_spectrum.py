def compute_full_H(d):
    N = 1 << (d - 1)
    H = np.zeros((N, N), dtype=complex)
    for k in range(N):
        k3 = (3 * k) % N
        kinv3 = (pow(3, -1, N) * k) % N
        H[k3, k] += 1 + np.exp(-2j * np.pi * k / N)
        H[kinv3, k] += 1 + np.exp(2j * np.pi * kinv3 / N)
    return H

