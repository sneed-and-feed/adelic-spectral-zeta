"""
Task 5.3: Weil Explicit Formula Higher Moments
==============================================
DESIGNED FOR EXECUTION ON GOOGLE COLAB (CPU RUNTIME)

This script computes the spectral fluctuations N_fluc(T) of the Riemann 
Zeta zeros generated by the adèlic Dirac operator. 

It calculates:
  N(T) = Number of eigenvalues <= T
  N_weyl(T) = (T/2pi) * log(T / 2pi e) + 7/8
  N_fluc(T) = N(T) - N_weyl(T)

We then compute the 2nd moment (Variance) and 4th moment of N_fluc(T) 
and compare them against the Keating-Snaith Random Matrix Theory (GUE) 
predictions.

Matrix size: N=2000 (4001x4001 dense matrix). 
CPU execution time: ~20-40 seconds.
"""

import numpy as np
import scipy.linalg as la
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
import time
import mpmath

print("=" * 70)
print("TASK 5.3: SPECTRAL FLUCTUATION MOMENT STATISTICS (COLAB CPU)")
print("=" * 70)

N = 2000
dim = 2 * N + 1
P_MAX = 500
lam = 42.0
log_lam = np.log(lam)

# 1. Sieve primes
def sieve(n):
    is_prime = np.ones(n + 1, dtype=bool)
    is_prime[:2] = False
    for i in range(2, int(n**0.5) + 1):
        if is_prime[i]:
            is_prime[i*i::i] = False
    return np.where(is_prime)[0]

primes = sieve(P_MAX)

# 2. Build the Dirac Operator for Zeta(s)
print(f"Building {dim}x{dim} Adèlic Dirac Operator (N={N})...")
start_time = time.time()

n_vals = np.arange(-N, N + 1)
D0_diag = n_vals * np.pi / log_lam

gamma_shift = np.zeros(dim, dtype=complex)
for i, n in enumerate(n_vals):
    t = n * np.pi / log_lam
    s_val = 0.5 + 1j * t
    try:
        gamma_shift[i] = 0.5 * complex(mpmath.psi(0, s_val / 2.0))
    except:
        gamma_shift[i] = 0.0

xi = np.zeros(dim, dtype=complex)
for p in primes:
    phases = -1j * n_vals * np.pi * np.log(p) / log_lam
    xi += (np.log(p) / np.sqrt(p)) * np.exp(phases)
    
xi += gamma_shift
xi_norm = xi / np.linalg.norm(xi)
P = np.outer(xi_norm, np.conj(xi_norm))
D = (np.eye(dim) - P) @ np.diag(D0_diag) @ (np.eye(dim) - P)

print(f"Operator built in {time.time() - start_time:.2f}s.")

# 3. Compute Eigenvalues
print("Diagonalising matrix (this may take ~20 seconds on CPU)...")
start_diag = time.time()
evals = np.sort(np.abs(la.eigvalsh(D)))
evals = evals[evals > 1.0] # Filter 0 and noise
print(f"Eigendecomposition completed in {time.time() - start_diag:.2f}s.")

# 4. Fluctuation Statistics
T_min = max(10.0, evals[0])
T_max = evals[min(400, len(evals)-1)]
T_vals = np.linspace(T_min, T_max, 2000)

N_T = np.zeros_like(T_vals)
N_weyl = np.zeros_like(T_vals)

for i, T in enumerate(T_vals):
    N_T[i] = np.sum(evals <= T)
    # Riemann-von Mangoldt formula smooth part
    N_weyl[i] = (T / (2 * np.pi)) * np.log(T / (2 * np.pi * np.e)) + 7/8

N_fluc = N_T - N_weyl

# Compute moments over sliding windows
window_size = len(T_vals) // 4
var_empirical = []
var_GUE = []
T_centers = []

for i in range(0, len(T_vals) - window_size, window_size // 2):
    window_fluc = N_fluc[i:i+window_size]
    T_center = np.mean(T_vals[i:i+window_size])
    
    # 2nd Moment (Variance)
    variance = np.var(window_fluc)
    # GUE Theoretical Variance for Riemann zeros
    # Var ~ (1 / pi^2) * log(log(T / 2pi)) + ... roughly proportional to log(T)
    gue_var = (1.0 / (np.pi**2)) * np.log(T_center)
    
    var_empirical.append(variance)
    var_GUE.append(gue_var)
    T_centers.append(T_center)

var_empirical = np.array(var_empirical)
var_GUE = np.array(var_GUE)

# Print overall moments
print("\n--- GLOBAL MOMENT STATISTICS ---")
print(f"Mean Fluctuation <N_fluc>: {np.mean(N_fluc):.4f} (Expected: ~0)")
print(f"2nd Moment <N_fluc^2>:     {np.mean(N_fluc**2):.4f}")
print(f"4th Moment <N_fluc^4>:     {np.mean(N_fluc**4):.4f}")
print(f"Kurtosis:                  {np.mean(N_fluc**4) / (np.mean(N_fluc**2)**2):.4f} (GUE Expected: ~3.0)")

# 5. Plotting
fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(10, 8))
fig.patch.set_facecolor('#0f0f1a')

for ax in [ax1, ax2]:
    ax.set_facecolor('#0f0f1a')
    ax.tick_params(colors='white')
    for spine in ax.spines.values(): spine.set_edgecolor('#444')

# Plot 1: Fluctuation Function
ax1.plot(T_vals, N_fluc, color='#4cc9f0', linewidth=1)
ax1.axhline(0, color='#f72585', linestyle='--', linewidth=2)
ax1.set_title("Spectral Fluctuations $N_{fluc}(T) = N(T) - N_{weyl}(T)$", color='white')
ax1.set_ylabel("$N_{fluc}(T)$", color='white')
ax1.grid(True, linestyle='--', alpha=0.3, color='#555')

# Plot 2: Variance Growth
ax2.plot(T_centers, var_empirical, 'o-', color='#4cc9f0', label='Empirical Variance $\langle N_{fluc}^2 \\rangle$')
ax2.plot(T_centers, var_GUE, 'x--', color='#f72585', label='GUE Prediction $\sim \\frac{1}{\pi^2} \log(T)$')
ax2.set_title("Variance Growth vs GUE Random Matrix Theory", color='white')
ax2.set_xlabel("Height $T$", color='white')
ax2.set_ylabel("Variance", color='white')
ax2.legend(facecolor='#1a1a2e', labelcolor='white')
ax2.grid(True, linestyle='--', alpha=0.3, color='#555')

plt.tight_layout()
plt.savefig('moment_statistics.png', dpi=300, facecolor=fig.get_facecolor())
plt.close()

print("\nPlot saved to moment_statistics.png")
print("=" * 70)
print("TASK 5.3 COMPLETE")
print("=" * 70)
